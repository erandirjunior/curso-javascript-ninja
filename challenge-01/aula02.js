// Operadores Aritméticos -> + - * /

1 + 2; //3

10 - 3; //7

3 * 3; //9

10 / 2; //5

// Espaço em branco é desconsiderado

10                             /2; //5

50/5; //10

// Operadores Aritméticos abreviados -> ++ -- 

var soma = 10 + 10; //undefined

soma; //20

soma = soma + 1; //21

soma; //21

soma++; //21

soma; //22

++soma; //23

soma; //23

soma = soma - 1; //22

soma; //22

soma--; //22

soma; //21

--soma; //20

soma; //20

// Operadores Aritméticos abreviados -> += -= *= /=

soma; //20

soma++; //20

soma; //21

soma += 20; //41

soma; //41

soma -= 20; //21

soma; //21

soma--; //21

soma; //20
