// Operadores Unários -> + e - //undefined

3 + 3; //6

+3; //3

+'3'; //3

// NaN not a number -> retornando quando uma operação matemática falha ou quando uma função tenta transformar uma string em inteiro

+'junior'; //NaN

// Concatenação

'jun' + 'ior'; //'junior'

'3' + 3; //'33'

3 + '3'; //'33'

var x = 3; //undefined

x; //3

-x; //-3

x; //3

-'3'; //-3