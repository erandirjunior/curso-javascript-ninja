
// Operadores Lógicos -> combinam ou invertem valores booleano

// && (AND) -> combinam dois valores e verifica se a combinação desses valores é true ou false

var x = 3; //undefined

var y = 7; //undefined

x == 3; //true

x === 3; //true

y === 5; //false

y === '7'; //false

y === 7; //true

x === 3 && y === 7; //true

x === 3 && y === 4; //false

// || (OR) -> combina dois valores e verifica se pelo menos um deles é true 

x === 3 || y === 3; //true

// ! (NOT) -> inverte um valor

x === 3; //true

!x === 3; //false

x != 3; //false

x !== 3; //false

x != '3'; //false

x !== '3'; //true
