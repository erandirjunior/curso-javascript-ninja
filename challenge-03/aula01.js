// Tipos de Dados

// Tipos Primitivos -> number, string, boolean, null e undefined

// Tipos de Objeto -> todos os outros tipos de dados

// Objeto -> conjunto de propriedades

{ propriedade: 'valor' } //{ propriedade: 'valor' }

var objeto = {propriedade: 'valor', propriedade2: 10, propriedade3: true}; //undefined

objeto; //{ propriedade: 'valor', propriedade2: 10, propriedade3: true }

objeto.propriedade; //'valor'

var pessoa = {nome: 'Junior', idade: 22, peso: 53, altura: 1.61}; //undefined

pessoa; //{ nome: 'Junior', idade: 22, peso: 53, altura: 1.61 }

pessoa.nome; //'Junior'

pessoa.idade; //22

pessoa.altura; //1.61

var carro = {cor: 'vermelho', motor: '2.0', portas: 4}; //undefined

carro; //{ cor: 'vermelho', motor: '2.0', portas: 4 }

carro.cor; //'vermelho'

carro.motor; //'2.0'

carro.portas; //4

pessoa; //{ nome: 'Junior', idade: 22, peso: 53, altura: 1.61 }

var myvar = function() { return 'variável myvar'; }; //undefined

myvar; //[Function: myvar]

myvar(); //'variável myvar'